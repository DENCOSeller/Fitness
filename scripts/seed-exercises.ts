async function main() {
  console.log('🌱 Начинаю загрузку системных упражнений...');

  // Delete existing system exercises to avoid duplicates
  const deleted = await prisma.exercise.deleteMany({
    where: { isSystem: true },
  });
  console.log(`🗑️  Удалено старых системных упражнений: ${deleted.count}`);

  // Insert all exercises
  let count = 0;
  for (const ex of exercises) {
    await prisma.exercise.create({
      data: {
        userId: null,
        name: ex.name,
        muscleGroup: ex.muscleGroup,
        description: ex.description,
        muscleGroups: ex.muscleGroups,
        equipment: ex.equipment,
        difficulty: ex.difficulty,
        tips: ex.tips,
        type: ex.type || (ex.muscleGroup === 'Кардио' ? 'cardio' : ex.muscleGroup === 'Растяжка' ? 'flexibility' : 'strength'),
        isSystem: true,
      },
    });
    count++;
  }

  console.log(`✅ Загружено системных упражнений: ${count}`);
  console.log('Категории:');

  const categories = new Map<string, number>();
  for (const ex of exercises) {
    categories.set(ex.muscleGroup, (categories.get(ex.muscleGroup) || 0) + 1);
  }
  for (const [cat, num] of categories) {
    console.log(`  ${cat}: ${num}`);
  }
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
